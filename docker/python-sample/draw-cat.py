import turtle

# Set up the turtle
t = turtle.Turtle()
t.speed(0)

# Draw the cat's head
t.penup()
t.goto(0, 100)
t.pendown()
t.begin_fill()
t.circle(75)
t.end_fill()

# Draw the cat's ears
t.penup()
t.goto(-45, 165)
t.pendown()
t.begin_fill()
t.circle(25)
t.end_fill()
t.penup()
t.goto(45, 165)
t.pendown()
t.begin_fill()
t.circle(25)
t.end_fill()

# Draw the cat's eyes
t.penup()
t.goto(-30, 130)
t.pendown()
t.begin_fill()
t.circle(10)
t.end_fill()
t.penup()
t.goto(30, 130)
t.pendown()
t.begin_fill()
t.circle(10)
t.end_fill()

# Draw the cat's nose and mouth
t.penup()
t.goto(0, 110)
t.pendown()
t.pencolor('black')
t.width(2)
t.right(90)
t.forward(15)
t.right(90)
t.forward(25)
t.right(90)
t.forward(15)

# Draw the cat's whiskers
t.penup()
t.goto(-25, 105)
t.pendown()
t.pencolor('black')
t.width(1)
t.right(110)
t.forward(25)
t.penup()
t.goto(-25, 100)
t.pendown()
t.right(-20)
t.forward(25)
t.penup()
t.goto(25, 105)
t.pendown()
t.right(140)
t.forward(25)
t.penup()
t.goto(25, 100)
t.pendown()
t.right(20)
t.forward(25)

# Hide the turtle and display the drawing
t.hideturtle()
turtle.done()
